public class IncDecOperatorsDemo
{  

        public static void main(String args[])
        {
		  				int a=1;
						int b=1;
						int c=1;
						int d=1;
						
		            System.out.println("pre-fix increment a: " + ++a);
					System.out.println("post-fix increment b: " + b++);
					System.out.println("Current value in a: " + a + " Current value in b: " + b);
					System.out.println("pre-fix decrement c: " + --c);
					System.out.println("post-fix decrement d: " + d--);
					 System.out.println("Current value in c: " + c + " Current value in d: " + d);						
 
			}
}
